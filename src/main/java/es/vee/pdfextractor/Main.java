	private void btnSelPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelPDFActionPerformed

		JFileChooser fileChooser = new JFileChooser();
		FileSystemView fw = fileChooser.getFileSystemView();
		fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);

		fileChooser.setAcceptAllFileFilterUsed(false);
		fileChooser.setCurrentDirectory(fw.getDefaultDirectory());
		fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("Portable Document Format", "pdf"));
		int returnVal = fileChooser.showOpenDialog(null);
		if (returnVal == JFileChooser.APPROVE_OPTION) {
			File sFile = fileChooser.getSelectedFile().getAbsoluteFile();
			boolean generate = true;
			if (sFile.exists()) {
				lblPdf.setText(sFile.getAbsolutePath());
				pdfFile = sFile;
				enableExtract();
				btnSelRTF.setEnabled(true);
			}
		}
	}//GEN-LAST:event_btnSelPDFActionPerformed

	private void enableExtract() {
		boolean enabled = (pdfFile != null && rtfFile != null);
		btnExtract.setEnabled(enabled);
	}

	private void btnSelRTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelRTFActionPerformed
		JFileChooser fileChooser = new JFileChooser();
		FileSystemView fw = fileChooser.getFileSystemView();
		fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        File defaultFile = new File(pdfFile.getName().replaceFirst(".pdf",".rtf"));
		fileChooser.setSelectedFile(defaultFile);
		fileChooser.setAcceptAllFileFilterUsed(false);
		fileChooser.setCurrentDirectory(fw.getDefaultDirectory());
		fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("RTF Ricth Text Format", "rtf"));
		int returnVal = fileChooser.showSaveDialog(null);
		if (returnVal == JFileChooser.APPROVE_OPTION) {

			File sFile = null;
			if (fileChooser.getSelectedFile().getName().endsWith(".rtf")) {
				sFile = new File(fileChooser.getSelectedFile().getAbsolutePath());
			} else {
				sFile = new File(fileChooser.getSelectedFile() + ".rtf");
			}

			boolean generate = true;
			if (sFile.exists()) {
				int reply = JOptionPane.showConfirmDialog(null, "El fichero ya existe. ¿Desea substituirlo?", 
						"Reemplazar Fichero", JOptionPane.YES_NO_OPTION);
				if (reply == JOptionPane.NO_OPTION) {
					generate = false;
				}
			}
			if (generate) {
				lblRtf.setText(sFile.getAbsolutePath());
				rtfFile = sFile;
				enableExtract();
			} else {
				showErrorMessage("El fichero existe. Seleccione otro nombre de fichero.", "Error al generar el fichero rtf con los resumenes");
			}
		}
	}//GEN-LAST:event_btnSelRTFActionPerformed

