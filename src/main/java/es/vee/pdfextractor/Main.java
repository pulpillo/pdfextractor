	private void resetForm(){

		this.pdfFile = null;
		this.rtfFile = null;
		lblPdf.setText("Seleccionar archivo PDF del que queremos extraer los resumenes");
		lblRtf.setText("Archivo RTF donde se guardarán los resúmenes");
		enableExtract();
		btnSelRTF.setEnabled(false);
		
	}

	protected class ExtractPDFWorker extends SwingWorker {

		private boolean extracted = false;

		@Override
		protected Object doInBackground() throws Exception {

			// Create Document object
			Document myDoc = new Document();
			try {
				PDFExtractor pdfExtract = PDFExtractor.create(pdfFile, textColor);
				List<PDFText> extractedTexts = pdfExtract.extract();

				RtfWriter2.getInstance(myDoc, new FileOutputStream(rtfFile));
				myDoc.open();

				for (PDFText text : extractedTexts) {
					Paragraph p = new Paragraph(text.getText());
					p.setFont(text.getFont());
					myDoc.add(p);
				}
				extracted = true;

			} catch (Exception ex) {
			} finally {
				myDoc.close();
			}

			return null;
		}

		@Override
		protected void done() {
			super.done();
			changeVisibility();
			if (extracted) {
				showMessage("Se ha generado el resumen.", "Resumen generado satisfactoriamente");
				if(chkOpenWhenFinish.isSelected()){
					try {
						Desktop dt = Desktop.getDesktop();
						dt.open(rtfFile);
					} catch (IOException ex) {
						Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
					}
				}
				
			} else {
				showErrorMessage("Se ha producido un error al generar el resumen", "Error al extraer el resumen");
			}
			resetForm();

		}
	}

	private void changeVisibility() {

		inProgress = !inProgress;

		if (inProgress) {
			lblProgress.setIcon(new javax.swing.ImageIcon(getClass().getResource("/loader_1.GIF"))); // NOI18N
			lblProgress.setText("Extrayendo resúmen ...");

		} else {
			lblProgress.setIcon(null); // NOI18N
			lblProgress.setText("Extractor de Resúmenes de PDF");
		}

		boolean enableControls = !inProgress;
		btnSelPDF.setEnabled(enableControls);
		btnSelRTF.setEnabled(enableControls);
		btnExtract.setEnabled(enableControls);
		btnSelTextColor.setEnabled(enableControls);
	}

